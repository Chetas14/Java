//Assignment Operator(=): used to assign a value to a variable
class Main 
{
    static public void main(String[] chetas)
    {
        int a=5;
        a+=5;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a*=5;
        System.out.println(a);
        a/=5;
        System.out.println(a);
        a%=5;
        System.out.println(a);
        
    }
}